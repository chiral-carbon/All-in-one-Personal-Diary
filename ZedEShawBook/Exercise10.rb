tabby_cat = "\tI'm tabbed in." # prints tab space
persian_cat = "I'm split\non a line." # prints a new line
backslash_cat = "I'm \\ a \\ cat." # prints the backslash character

# a string within tripple quotes and containing a list
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
